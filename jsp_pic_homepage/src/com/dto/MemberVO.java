package com.dto;

public class MemberVO {

	private int id_num;
	private String id_id;
	private String id_pwd;
	private String id_nickname;
	private String id_email;
	private String id_tel;
	private String membercheck;

	public int getId_num() {
		return id_num;
	}

	public void setId_num(int id_num) {
		this.id_num = id_num;
	}

	public String getId_id() {
		return id_id;
	}

	public void setId_id(String id_id) {
		this.id_id = id_id;
	}

	public String getId_pwd() {
		return id_pwd;
	}

	public void setId_pwd(String id_pwd) {
		this.id_pwd = id_pwd;
	}

	public String getId_nickname() {
		return id_nickname;
	}

	public void setId_nickname(String id_nickname) {
		this.id_nickname = id_nickname;
	}

	public String getId_email() {
		return id_email;
	}

	public void setId_email(String id_email) {
		this.id_email = id_email;
	}

	public String getId_tel() {
		return id_tel;
	}

	public void setId_tel(String id_tel) {
		this.id_tel = id_tel;
	}

	public String getMembercheck() {
		return membercheck;
	}

	public void setMembercheck(String membercheck) {
		this.membercheck = membercheck;
	}
	
	
	

}
